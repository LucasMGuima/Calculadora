#include <math.h>

#include "./include/operations.h"

TYPE sum(TYPE a, TYPE b){ return a + b; }

TYPE sub(TYPE a, TYPE b){ return a - b; }

TYPE mult(TYPE a, TYPE b){ return a * b; }

TYPE divi(TYPE a, TYPE b){ return a / b; }

TYPE root(TYPE a, TYPE b){ return pow(a, (1.0/b));}

TYPE power(TYPE a, TYPE b){ return pow(a, b);}